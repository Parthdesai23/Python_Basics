def add(a,s):
    ans=a+s
    print('add=',ans)
def sub(a,s):
    ans=a-s
    print('sub=',ans)
def mul(a,s):
    ans=a*s
    print('mul=',ans)
def div(a,s):
    ans=a/s
    print('div=',ans)
def flo(a,s):
    ans=a//s
    print('floor=',ans)
def mod(a,s):
    ans=a%s
    print('mod=',ans)
def exp(a,s):
    ans=a**s
    print('exp=',ans)

if __name__=="__main__":
    add(1,2)
